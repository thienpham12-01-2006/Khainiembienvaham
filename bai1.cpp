#include <stdio.h>

int main() {
    // Kieu so nguyen: int - Luu tru so nguyen (khong co phan thap phan).
    int tuoi = 25; // Vi du: tuoi cua mot nguoi.

    // Kieu so thuc: float - Luu tru so thap phan voi do chinh xac don.
    float chieuCao = 1.75; // Vi du: chieu cao tinh bang met.

    // Kieu so thuc: double - Luu tru so thap phan voi do chinh xac cao hon float.
    double pi = 3.14159265359; // Vi du: gia tri cua so Pi.

    // Kieu ky tu: char - Luu tru mot ky tu ASCII.
    char diem = 'A'; // Vi du: xep loai hoc tap.

    // Kieu logic: _Bool - Luu tru gia tri dung (1) hoac sai (0).
    _Bool laSinhVien = 1; // 1 la true, 0 la false.
}

